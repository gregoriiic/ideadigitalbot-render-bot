function translate(locale, key, vars = {}) {
  const code = normalizeLocale(locale);
  const catalog = STRINGS[code] || STRINGS.es;
  const template = catalog[key] || STRINGS.es[key] || key;

  return String(template).replace(/\{([a-z_]+)\}/gi, (_, token) => {
    return Object.prototype.hasOwnProperty.call(vars, token) ? String(vars[token]) : `{${token}}`;
  });
}

